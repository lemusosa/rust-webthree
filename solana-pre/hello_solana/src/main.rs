fn main() {
    // Variables and simple printing
    let a: i32 = 10;
    let b: i32 = 15;
    println!("Hello, world!, {} {}", a,b);

    // Dat types

    // unsigned integer
    // u8, u16, u32, u64, u128
    let unsigned: u32 = 10;

    // signed integer
    // i8, i186, i32, i64, i128
    let signed: i32 = -10;

    // float is used for decimals: f64 offers more precision
    let float: f64 = 0.32;

    println!("Different numbers => {} {} {}", unsigned, signed, float);

}
